package br.com.desafio.dominio;

public class Destino {
	
	String cidade;
	int distancia;
	
	public Destino() {}
	
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public int getDistancia() {
		return distancia;
	}
	public void setDistancia(int distancia) {
		this.distancia = distancia;
	}
	
}
